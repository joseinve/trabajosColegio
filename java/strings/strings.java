package strings;

public class strings {
	public static void main(String[] args) {
		String n;
		n = "123456";
		System.out.println(n.length());
		System.out.println(n.substring(2));
		System.out.println("hola".toUpperCase());
		System.out.println("ADIOS".toLowerCase());
		System.out.println("hola".equals(n));
	}
}
